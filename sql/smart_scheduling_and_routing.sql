-- ---------------------------------------------------------------------------
-- Scheduling tables
-- ---------------------------------------------------------------------------

create table if not exists public.shift_templates (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  name text not null,
  site_id bigint null references public.sites(id) on delete set null,
  start_time time not null,
  end_time time not null,
  required_headcount int not null default 1 check (required_headcount > 0),
  branch text null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.shifts (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  site_id bigint null references public.sites(id) on delete set null,
  title text not null,
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  required_headcount int not null default 1 check (required_headcount > 0),
  branch text null,
  geofence_radius_m int not null default 200 check (geofence_radius_m > 0),
  status text not null default 'open' check (status in ('open', 'filled', 'in_progress', 'completed', 'cancelled')),
  notes text null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (ends_at > starts_at)
);

create index if not exists idx_shifts_company_starts on public.shifts(company_id, starts_at);
create index if not exists idx_shifts_company_status on public.shifts(company_id, status);

create table if not exists public.shift_assignments (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  shift_id bigint not null references public.shifts(id) on delete cascade,
  employee_id bigint not null references public.employees(id) on delete cascade,
  status text not null default 'offered' check (status in ('offered', 'accepted', 'declined', 'no_show', 'completed')),
  score numeric(10,2) null,
  score_reason text null,
  offered_at timestamptz not null default now(),
  responded_at timestamptz null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (shift_id, employee_id)
);

create index if not exists idx_shift_assignments_company_employee on public.shift_assignments(company_id, employee_id, status);
create index if not exists idx_shift_assignments_shift_status on public.shift_assignments(shift_id, status);

create table if not exists public.shift_events (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  shift_id bigint not null references public.shifts(id) on delete cascade,
  assignment_id bigint null references public.shift_assignments(id) on delete set null,
  employee_id bigint null references public.employees(id) on delete set null,
  event_type text not null check (event_type in ('late_checkin', 'no_show', 'overtime_exception', 'geofence_block', 'window_block', 'manual_override')),
  details jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists idx_shift_events_company_created on public.shift_events(company_id, created_at desc);

-- ---------------------------------------------------------------------------
-- Recipient routing and notifications
-- ---------------------------------------------------------------------------

create table if not exists public.submission_recipients (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  submission_type text not null check (submission_type in ('incident', 'job_request')),
  submission_id bigint not null,
  recipient_user_id uuid not null references auth.users(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (submission_type, submission_id, recipient_user_id)
);

create index if not exists idx_submission_recipients_company_recipient on public.submission_recipients(company_id, recipient_user_id, created_at desc);

create table if not exists public.notification_queue (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  recipient_user_id uuid not null references auth.users(id) on delete cascade,
  channel text not null default 'in_app' check (channel in ('in_app', 'email', 'webhook')),
  source text not null,
  title text not null,
  body text not null,
  payload jsonb not null default '{}'::jsonb,
  status text not null default 'pending' check (status in ('pending', 'sent', 'failed')),
  sent_at timestamptz null,
  created_at timestamptz not null default now()
);

create index if not exists idx_notification_queue_company_recipient on public.notification_queue(company_id, recipient_user_id, status, created_at desc);

