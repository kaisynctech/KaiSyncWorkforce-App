create index if not exists idx_employee_job_requests_company_status on public.employee_job_requests(company_id, status, created_at desc);

-- ---------------------------------------------------------------------------
-- Trigger helper for updated_at
-- ---------------------------------------------------------------------------

create or replace function public.smart_sched_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

do $$
begin
  execute 'drop trigger if exists trg_shift_templates_updated_at on public.shift_templates';
  execute 'create trigger trg_shift_templates_updated_at before update on public.shift_templates for each row execute function public.smart_sched_touch_updated_at()';
  execute 'drop trigger if exists trg_shifts_updated_at on public.shifts';
  execute 'create trigger trg_shifts_updated_at before update on public.shifts for each row execute function public.smart_sched_touch_updated_at()';
  execute 'drop trigger if exists trg_shift_assignments_updated_at on public.shift_assignments';
  execute 'create trigger trg_shift_assignments_updated_at before update on public.shift_assignments for each row execute function public.smart_sched_touch_updated_at()';
  execute 'drop trigger if exists trg_employee_job_requests_updated_at on public.employee_job_requests';
  execute 'create trigger trg_employee_job_requests_updated_at before update on public.employee_job_requests for each row execute function public.smart_sched_touch_updated_at()';
end;
$$;

