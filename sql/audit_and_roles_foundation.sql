-- ---------------------------------------------------------------------------
-- 6) RLS for audit logs (HR sees only own company audit)
-- ---------------------------------------------------------------------------

alter table public.audit_logs enable row level security;
alter table public.audit_logs force row level security;

drop policy if exists p_audit_logs_select_own_company on public.audit_logs;
create policy p_audit_logs_select_own_company
on public.audit_logs
for select
to authenticated
using (
  exists (
    select 1
    from public.hr_users h
    where h.auth_user_id = auth.uid()
      and h.is_active = true
      and h.company_id = audit_logs.company_id
  )
);

revoke all on public.audit_logs from anon;
grant select on public.audit_logs to authenticated;
