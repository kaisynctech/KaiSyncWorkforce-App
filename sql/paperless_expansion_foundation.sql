-- Paperless expansion foundation
-- Covers:
-- 1) Digital forms + approvals + signature-ready submissions
-- 2) Document vault with versioning metadata
-- 3) Compliance tracking + assignment guardrails
-- 4) Client handover packs
-- 5) Export scheduling + integrations + automation rules

set search_path = public;

-- ---------------------------------------------------------------------------
-- Phase 1: forms + approvals
-- ---------------------------------------------------------------------------

create table if not exists public.form_templates (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  name text not null,
  form_type text not null,
  schema_json jsonb not null default '{}'::jsonb,
  requires_employee_signature boolean not null default false,
  requires_supervisor_signature boolean not null default false,
  requires_client_signature boolean not null default false,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_form_templates_company on public.form_templates(company_id, is_active);

create table if not exists public.form_submissions (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  template_id bigint not null references public.form_templates(id) on delete restrict,
  employee_id bigint null references public.employees(id) on delete set null,
  job_id bigint null references public.jobs(id) on delete set null,
  status text not null default 'draft' check (status in ('draft', 'submitted', 'approved', 'rejected')),
  payload_json jsonb not null default '{}'::jsonb,
  employee_signature_url text null,
  supervisor_signature_url text null,
  client_signature_url text null,
  signed_pdf_url text null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_form_submissions_company_status on public.form_submissions(company_id, status, created_at desc);

create table if not exists public.form_approvals (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  submission_id bigint not null references public.form_submissions(id) on delete cascade,
  step_order int not null default 1,
  required_role text not null default 'manager',
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected')),
  approver_user_id uuid null references auth.users(id) on delete set null,
  comments text null,
  acted_at timestamptz null,
  created_at timestamptz not null default now(),
  unique (submission_id, step_order)
);

create index if not exists idx_form_approvals_submission on public.form_approvals(submission_id, step_order);

-- ---------------------------------------------------------------------------
-- Phase 1: document vault
