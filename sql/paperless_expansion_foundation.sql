create index if not exists idx_document_files_company_created on public.document_files(company_id, created_at desc);
create index if not exists idx_document_files_employee on public.document_files(company_id, employee_id);
create index if not exists idx_document_files_job on public.document_files(company_id, job_id);

-- ---------------------------------------------------------------------------
-- Phase 2: compliance + handover
-- ---------------------------------------------------------------------------

create table if not exists public.compliance_requirements (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  title text not null,
  applies_to_job_category text null,
  validity_days int null,
  is_mandatory boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.employee_compliance_records (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  employee_id bigint not null references public.employees(id) on delete cascade,
  requirement_id bigint not null references public.compliance_requirements(id) on delete cascade,
  document_file_id bigint null references public.document_files(id) on delete set null,
  issued_on date null,
  expires_on date null,
  status text not null default 'valid' check (status in ('valid', 'expiring', 'expired', 'missing')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (employee_id, requirement_id)
);

create index if not exists idx_employee_compliance_company_status on public.employee_compliance_records(company_id, status, expires_on);

create table if not exists public.handover_packs (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  job_id bigint not null references public.jobs(id) on delete cascade,
  generated_pdf_url text null,
  checklist_json jsonb not null default '{}'::jsonb,
  shared_with_email text null,
  shared_at timestamptz null,
  download_token text null,
  download_expires_at timestamptz null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (job_id)
);

