-- ---------------------------------------------------------------------------
-- Updated-at trigger helper reuse
-- ---------------------------------------------------------------------------

create or replace function public.paperless_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

do $$
begin
  execute 'drop trigger if exists trg_form_templates_updated_at on public.form_templates';
  execute 'create trigger trg_form_templates_updated_at before update on public.form_templates for each row execute function public.paperless_touch_updated_at()';
  execute 'drop trigger if exists trg_form_submissions_updated_at on public.form_submissions';
  execute 'create trigger trg_form_submissions_updated_at before update on public.form_submissions for each row execute function public.paperless_touch_updated_at()';
  execute 'drop trigger if exists trg_document_files_updated_at on public.document_files';
  execute 'create trigger trg_document_files_updated_at before update on public.document_files for each row execute function public.paperless_touch_updated_at()';
  execute 'drop trigger if exists trg_employee_compliance_records_updated_at on public.employee_compliance_records';
  execute 'create trigger trg_employee_compliance_records_updated_at before update on public.employee_compliance_records for each row execute function public.paperless_touch_updated_at()';
  execute 'drop trigger if exists trg_handover_packs_updated_at on public.handover_packs';
  execute 'create trigger trg_handover_packs_updated_at before update on public.handover_packs for each row execute function public.paperless_touch_updated_at()';
  execute 'drop trigger if exists trg_scheduled_exports_updated_at on public.scheduled_exports';
  execute 'create trigger trg_scheduled_exports_updated_at before update on public.scheduled_exports for each row execute function public.paperless_touch_updated_at()';
  execute 'drop trigger if exists trg_integration_endpoints_updated_at on public.integration_endpoints';
  execute 'create trigger trg_integration_endpoints_updated_at before update on public.integration_endpoints for each row execute function public.paperless_touch_updated_at()';
  execute 'drop trigger if exists trg_automation_rules_updated_at on public.automation_rules';
  execute 'create trigger trg_automation_rules_updated_at before update on public.automation_rules for each row execute function public.paperless_touch_updated_at()';
end;
$$;

-- ---------------------------------------------------------------------------
-- Optional compliance guardrail for employee assignment
-- ---------------------------------------------------------------------------

create or replace function public.has_employee_mandatory_compliance(
  p_company_id bigint,
  p_employee_id bigint
)
returns boolean
language sql
stable
as $$
  select not exists (
    select 1
    from public.compliance_requirements r
    where r.company_id = p_company_id
      and r.is_mandatory = true
      and not exists (
        select 1
        from public.employee_compliance_records c
        where c.company_id = p_company_id
          and c.employee_id = p_employee_id
          and c.requirement_id = r.id
          and c.status = 'valid'
          and (c.expires_on is null or c.expires_on >= current_date)
      )
  );
$$;
